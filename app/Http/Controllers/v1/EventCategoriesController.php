    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $data = $request->all();
        $eventId = $data['event_id'];
        unset($data['event_id']);

        foreach ($data as $category => $data){
            $existingCategory = EventCategories::where(EventCategories::COL_NAME,$category)->where(EventCategories::COL_EVENT_ID,$eventId)->first();
            if(!$existingCategory) {
                EventCategories::create([
                    EventCategories::COL_EVENT_ID => $eventId,
                    EventCategories::COL_NAME => $category
                ]);
            }
        }

        return $this->responseCreated('Categories added');
    }

